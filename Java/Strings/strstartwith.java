public class strstartwith
{
	public static void main(String[] args) 
	{
		String str1 = "Shiny JK welcome to java";

		if(str1.startsWith("Shiny"))
		{
			System.out.println("String starts with Shiny");
		}
		else
		{
			System.out.println("String does not start with Shiny");
		}
	}
}